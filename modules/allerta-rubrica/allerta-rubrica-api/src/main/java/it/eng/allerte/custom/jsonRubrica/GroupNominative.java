package it.eng.allerte.custom.jsonRubrica;

public class GroupNominative {

	private Long nominativeId;
    private Long role;
    private String specificaRuolo;
    
	public Long getNominativeId() {
		return nominativeId;
	}
	public void setNominativeId(Long nominativeId) {
		this.nominativeId = nominativeId;
	}
	public Long getRole() {
		return role;
	}
	public void setRole(Long role) {
		this.role = role;
	}
	public String getSpecificaRuolo() {
		return specificaRuolo;
	}
	public void setSpecificaRuolo(String specificaRuolo) {
		this.specificaRuolo = specificaRuolo;
	}
	
    
    
}
