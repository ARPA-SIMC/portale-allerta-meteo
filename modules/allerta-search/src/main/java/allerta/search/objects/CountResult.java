package allerta.search.objects;

public class CountResult {
	
	String periodo;
	String periodoVisualizzato;
	int count;
	
	public String getPeriodo() {
		return periodo;
	}
	public void setPeriodo(String periodo) {
		this.periodo = periodo;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public String getPeriodoVisualizzato() {
		return periodoVisualizzato;
	}
	public void setPeriodoVisualizzato(String periodoVisualizzato) {
		this.periodoVisualizzato = periodoVisualizzato;
	}
	
	

}
